/** Get a niche by slug. */
export const getNicheBySlug = (slug: string): NicheData | undefined =>
  nicheDatabase.find(n => n.slug === slug);

/** Get comparison niches (same category, excluding current). */
export const getComparisonNiches = (slug: string, count = 4): NicheData[] => {
  const current = getNicheBySlug(slug);
  if (!current) return nicheDatabase.slice(0, count);
  const sameCategory = nicheDatabase.filter(n => n.category === current.category && n.slug !== slug);
  const others = nicheDatabase.filter(n => n.category !== current.category && n.slug !== slug);
  return [...sameCategory, ...others].slice(0, count);
};

/** All niche slugs for sitemap/routing. */
export const allNicheSlugs = nicheDatabase.map(n => n.slug);
